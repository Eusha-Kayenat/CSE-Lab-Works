from collections import deque

def bfs(goal, adj, n):
    d = {i: float('inf') for i in range(1,n+1)}
    d[goal] = 0
    q = deque([goal])

    while q:
        u = q.popleft()
        for v in adj[u]:
            if d[v] == float('inf'):
                d[v] = d[u] + 1
                q.append(v)
    return d

# input
